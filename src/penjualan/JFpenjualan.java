    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
          try {
    st = cn.createStatement();
    st.executeUpdate("INSERT INTO penjualanalbum VALUES('" + txtId.getText() + "','"
        + txtNama.getText() + "','"
        + txtNim.getText() + "','"
        + cmbJudul.getSelectedItem() + "','"
        + cmbJenis.getSelectedItem() + "','"
        + txtStock.getText() + "','"
        + txtHarga.getText() + "','"
        + txtTotal.getText() + "')");
    tampilData("");
    JOptionPane.showMessageDialog(null, "Simpan Berhasil");
    txtId.setText("");
    txtNama.setText("");
    txtNim.setText("");
    cmbJudul.setSelectedItem("");
    cmbJenis.setSelectedItem("");
    txtStock.setText("");
    txtHarga.setText("");
    txtTotal.setText("");
       } catch (Exception e) {
         e.printStackTrace();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

