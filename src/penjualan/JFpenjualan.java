    public void tampilData(String where) {
        try {
            st = cn.createStatement();
             tabModel.getDataVector().removeAllElements();
             tabModel.fireTableDataChanged();
             rs = st.executeQuery("SELECT * FROM penjualanalbum " + where);
    
            while (rs.next()) {
                Object[] data = {
                rs.getString("Id"),
                rs.getString("Nama"),
                rs.getString("Judul_Album"),
                rs.getString("Jenis"),
                rs.getString("Stock"),
                rs.getString("Harga"),
                rs.getString("Total"),
            };
        
            tabModel.addRow(data);
            }
        } catch(Exception e) {
            e.printStackTrace();
    }
}


